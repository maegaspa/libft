#include "libft.h"

int	ft_atoi(char *s)
{
	long	i;
	long	num;
	long	neg;

	i = 0;
	num = 0;
	neg = 0;
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t'
			|| s[i] == '\v' || s[i] == '\f' || s[i] == '\r')
		i++;
	if (s[i] == '+')
		i++;
	if (s[i] == '-' && s[i - 1] != '+')
	{
		neg = 1;
		i++;
	}
	while (s[i] >= 48 && s[i] <= 57)
	{
		num = (num * 10) + s[i] - 48;
		i++;
	}
	return (neg ? num *= -1 : num);
}

#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	int	i;

	i = -1;
	while (++i < (int)n)
		if (((unsigned char *)s)[i] == (unsigned char)c)
			return (&((unsigned char*)s)[i]);
	return (0);
}
